
/* Arithmetic operator
    + -- Addition
    - -- Subtraction
    * -- Multiplication
    ** -- Exponential
    / -- Division
    % -- Modulus(Remainder)
    ++ -- Increment
    -- -- Decrement
*/

/* Assignment Operator
    =    x = y
    +=   x = x + y
    -=   x = x - y
    *=   x = x * y
    /=   x = x / y
    %=   x = x % y
    **=  x = x ** y

    <<=   x = x << y
    >>=   x = x >> y
    >>>=  x = x >>> y
    &=    x = x & y
    ^=    x = x ^ y
    |=    x = x | y
*/

/* Comparison operator
    == equal to
    === equal value and equal type
    != not equal to
    !== not equal value and not equal type
    > greater than
    < less than
    >= greater than or equal to
    <= less than or equal to
    ? ternary operator
*/

/* logical operator
    && logical AND operator
    || logical OR operator
    ! logical NOT operator
*/

/* Type operator
    typeof --- returns the type of a variable
    instanceof --- returns true if an object is an instance of and object type
*/

/* Bitwise Operator
    & AND
    | OR
    ~ NOT
    ^ XOR
    << Zero fill left shift
    >> Signed right shift
    >>> Zero fill right shift
*/

